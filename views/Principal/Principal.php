              function EstadoCuenta ()
        {

            //alert ('test');
            var Dpi = $('#dpiInput').val ();

            if (Dpi == '')
            {
                alert ('INGRESE PRIMERO SU DPI');
            }
            else
            {

                $.ajax({
                    url: '?c=Pagos&a=getDatosporDpi', 
                    type: 'POST',     
                    dataType: 'json', 
                    data: {
                        Dpi: Dpi,
                    },
                    success: function(data) {
                        console.log(data);
                        var baseUrl = "?c=Pagos&a=EstadoCuenta";
                        var fullUrl = baseUrl + "&id=" + data[0].id;
                        window.location.href = fullUrl;
                    },
                    error: function(error) {
                        // Aquí manejas los errores que puedan surgir durante la petición
                        console.error("Error en la petición:", error);
                        alert ("dpi invalido o no registrado");
                    }
                });


            }
        }
    </script>


